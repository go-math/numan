func many(ni, no int) func([]float64, []float64, []uint64) {
	return func(x, y []float64, _ []uint64) {
		sum, value := 0.0, 0.0

		for i := 0; i < ni; i++ {
			sum += x[i]
		}

		if sum > float64(ni)/4 {
			value = 1
		}

		for i := 0; i < no; i++ {
			y[i] = value
		}
	}
}

var kraichnanOrszag = func(y0, ys []float64, _ []uint64) {
	const (
		step = 0.01
	)

	normalize := func(y []float64) []float64 {
		return []float64{2*y[0] - 1, 2*y[1] - 1, 2*y[2] - 1}
	}

	dydt := func(_ float64, y, f []float64) {
		f[0] = y[0] * y[2]
		f[1] = -y[1] * y[2]
		f[2] = -y[0]*y[0] + y[1]*y[1]
	}

	config := rk4.Config{Step: step}
	integrator, _ := rk4.New(&config)

	Ys, _, _ := integrator.Compute(dydt, normalize(y0), []float64{0, 3000 * step})

	if len(ys) != 3*301 || len(Ys) != 3*3001 {
		panic("something went wrong")
	}

	for i := 0; i < 301; i++ {
		ys[3*i+0] = Ys[10*3*i+0]
		ys[3*i+1] = Ys[10*3*i+1]
		ys[3*i+2] = Ys[10*3*i+2]
	}
}

