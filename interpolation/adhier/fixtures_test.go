func hat(x, y []float64, _ []uint64) {
	z := 5*x[0] - 1

	switch {
	case 0 <= z && z < 1:
		y[0] = 0.5 * z * z
	case 1 <= z && z < 2:
		y[0] = 0.5 * (-2*z*z + 6*z - 3)
	case 2 <= z && z < 3:
		y[0] = 0.5 * (3 - z) * (3 - z)
	default:
		y[0] = 0
	}
}

