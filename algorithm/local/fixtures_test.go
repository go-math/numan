type fixture struct {
	rule string

	config func() *Config
	target func() Target
	metric func() Metric

	compute func([]float64, []float64)

	surrogate *Surrogate

	levels []uint64
	orders []uint64

	points []float64
	values []float64

	integral []float64
}

func (self *fixture) initialize() {
	self.surrogate.Indices = make([]uint64, len(self.levels))
	for i := range self.levels {
		self.surrogate.Indices[i] = self.levels[i] | self.orders[i]<<levelSize
	}
}

type metric struct {
	tolerance float64
}

func (self *metric) Score(location *Location) float64 {
	for _, ε := range location.Surplus {
		if math.Abs(ε) > self.tolerance {
			return 1.0
		}
	}
	return 0.0
}

func prepare(fixture *fixture, arguments ...interface{}) (*Interpolator, Target, Metric) {
	const (
		tolerance = 1e-4
	)

	var config *Config
	if fixture.config == nil {
		config = NewConfig()
	} else {
		config = fixture.config()
	}

	if len(arguments) > 0 {
		arguments[0].(func(*Config))(config)
	}

	var target Target
	if fixture.target == nil {
		target = newTarget(fixture.surrogate.Inputs, fixture.surrogate.Outputs, fixture.compute)
	} else {
		target = fixture.target()
	}

	ni, no := target.Dimensions()

	var metric Metric
	if fixture.metric == nil {
		metric = newMetric(no, tolerance)
	} else {
		metric = fixture.metric()
	}

	switch fixture.rule {
	case "open":
		return New(equidistant.NewOpen(ni), linear.NewOpen(ni), config), target, metric
	default:
		return New(equidistant.NewClosed(ni), linear.NewClosed(ni), config), target, metric
	}
}

func newTarget(ni, no uint, compute func([]float64, []float64)) Target {
	return NewTarget(ni, no, compute)
}

func newMetric(no uint, tolerance float64) Metric {
	return &metric{tolerance}
}

